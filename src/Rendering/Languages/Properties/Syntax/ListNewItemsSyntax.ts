/**
 * Metadata on a language's list creation with items syntax.
 */
export class ListNewItemsSyntax {
    /**
     * Characters before each item in the list, if there are items.
     */
    public itemLeft: string;

    /**
     * Characters after each item in the list, if there are items.
     */
    public itemRight: string;

    /**
     * Characters before items in the list, if there are items.
     */
    public left: string;

    /**
     * Characters after items in the list, if there are items.
     */
    public right: string;
}
